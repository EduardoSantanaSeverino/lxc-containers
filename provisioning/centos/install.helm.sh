# Install a Helm

wget https://get.helm.sh/helm-v3.3.1-linux-amd64.tar.gz

tar zxf helm-v3.3.1-linux-amd64.tar.gz

cd linux-amd64/

sudo mv helm /usr/local/bin

which helm

